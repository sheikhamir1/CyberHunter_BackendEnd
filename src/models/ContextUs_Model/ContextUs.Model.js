const mongoose = require("mongoose");

const contextUsSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    subject: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const ContextUs = mongoose.model("ContextUs", contextUsSchema);

module.exports = ContextUs;
